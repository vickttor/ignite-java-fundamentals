package fundamentals.wrapper;

public class WrapperClass {

    public static void main(String[] args) {
        // in primitive types we can't pass null as value.
        // In wrapper classes we can assign null

        int intType = 1;
        float floatType = 2.0f;
        boolean booleanType = false;

        // int - char
        Float floatType2 = 1F;
        Boolean booleanType2 = false;
        Integer intType2 = 2;

    }
}
